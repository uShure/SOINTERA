    if (lowerMessage.includes('—Ñ—É–Ω–¥–∞–º–µ–Ω—Ç')) return '–§—É–Ω–¥–∞–º–µ–Ω—Ç';
    if (lowerMessage.includes('–¥–Ω–∫') || lowerMessage.includes('—Ü–≤–µ—Ç')) return '–î–ù–ö —Ü–≤–µ—Ç–∞';
    if (lowerMessage.includes('–Ω–∞—Å—Ç–∞–≤–Ω–∏–∫')) return '–ù–∞—Å—Ç–∞–≤–Ω–∏–∫';
    if (lowerMessage.includes('—Å—Ç—Ä–∏–∂–∫')) return '–°—Ç—Ä–∏–∂–∫–∏';
    if (lowerMessage.includes('–∫–æ–ª–æ—Ä–∏—Å—Ç')) return '–ö–æ–ª–æ—Ä–∏—Å—Ç–∏–∫–∞';

    // –ê–Ω–∞–ª–∏–∑ –∏—Å—Ç–æ—Ä–∏–∏
    const allMessages = conversationHistory.map(m => m.content.toLowerCase()).join(' ');
    if (allMessages.includes('—Ñ—É–Ω–¥–∞–º–µ–Ω—Ç')) return '–§—É–Ω–¥–∞–º–µ–Ω—Ç';
    if (allMessages.includes('–¥–Ω–∫') || allMessages.includes('—Ü–≤–µ—Ç')) return '–î–ù–ö —Ü–≤–µ—Ç–∞';

