  // Определяем какой курс предложить согласно воронке
  selectCourseForSalesFunnel(message: string, classification: string): string | null {
    const lowerMessage = message.toLowerCase();

    // Для опытных мастеров с запросом по стрижкам
    if (classification === 'experienced' &&
        (lowerMessage.includes('стрижк') || lowerMessage.includes('форм') || lowerMessage.includes('фундамент'))) {
      return 'fundamentals';
    }

    // Для опытных мастеров с запросом по колористике
    if (classification === 'experienced' &&
        (lowerMessage.includes('цвет') || lowerMessage.includes('колор') || lowerMessage.includes('днк'))) {
      return 'dna_color';
    }

    // Для тех, кто хочет преподавать стрижки
    if ((lowerMessage.includes('преподават') || lowerMessage.includes('обучат') ||
        lowerMessage.includes('наставник') || lowerMessage.includes('тренер')) &&
        lowerMessage.includes('стрижк')) {
      return 'mentor';
    }

    // Для тех, кто хочет преподавать колористику
    if ((lowerMessage.includes('преподават') || lowerMessage.includes('обучат') ||
        lowerMessage.includes('наставник') || lowerMessage.includes('тренер')) &&
        (lowerMessage.includes('колор') || lowerMessage.includes('цвет'))) {
      return 'mentor_color';
    }

    // Общий запрос на преподавание
    if (lowerMessage.includes('преподават') || lowerMessage.includes('обучат') ||
        lowerMessage.includes('наставник') || lowerMessage.includes('тренер')) {
      return 'mentor';
    }

    // По умолчанию для опытных - Фундамент
    if (classification === 'experienced') {
      return 'fundamentals';
    }

    return null;
  }

